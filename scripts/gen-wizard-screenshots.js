#!/usr/bin/env node
'use strict';
// Base64-inline the captured wizard screenshots (staged per-platform PNGs) into a committed,
// dual-mode module: module.exports in Node (build guard) / PConf.screenshots in the webview.
// The module is keyed by platform: S[platform][group][val] (radar as S[platform].radar), so the
// wizard shows the shot matching the user's watch. Precedent: src/pkjs/settings/bmc-badge.js.
// Reads staged files at <stageDir>/<platform>/<slug>.png. Run by capture-wizard-screenshots.sh.
const fs = require('fs');
const path = require('path');
const { SHOTS } = require('./gen-wizard-fixtures.js');
const OUT = path.join('src', 'pkjs', 'settings', 'wizard-screenshots.generated.js');
function dataUri(file) { return 'data:image/png;base64,' + fs.readFileSync(file).toString('base64'); }
function generate(stageDir) {
  const groups = {};
  for (const s of SHOTS) {
    const plats = String(s.platforms || '').split(/\s+/).filter(Boolean);
    for (const plat of plats) {
      const uri = dataUri(path.join(stageDir, plat, s.slug + '.png'));
      groups[plat] = groups[plat] || {};
      if (s.group === 'radar') { groups[plat].radar = uri; }
      else { (groups[plat][s.group] = groups[plat][s.group] || {})[s.val] = uri; }
    }
  }
  const body = 'var S = ' + JSON.stringify(groups, null, 0) + ';';
  const src = '// GENERATED by `mise capture-wizard-screenshots` — do not hand-edit.\n'
    + '// Committed on purpose (the emulator capture runs off-CI); the build hard-requires it.\n'
    + '/* global PConf */\n'
    + '(function () {\n  ' + body + '\n'
    + '  if (typeof module !== \'undefined\' && module.exports) { module.exports = S; }\n'
    + '  if (typeof PConf !== \'undefined\' && PConf) { PConf.screenshots = S; }\n'
    + '})();\n';
  fs.writeFileSync(OUT, src);
  return OUT;
}
if (require.main === module) {
  const stageDir = process.argv[2];
  if (!stageDir) { console.error('usage: gen-wizard-screenshots.js <stageDir>'); process.exit(2); }
  console.log('wrote ' + generate(stageDir));
}
module.exports = { generate };
